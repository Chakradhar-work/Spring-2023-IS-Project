function getBotResponse(input) {
    //rock paper scissors
    if (input == "rock") {
        return "";
    } else if (input == "paper") {
        return "";
    } else if (input == "") {
        return "";
    }

    // Simple responses
    if (input == "hello") {
        return "";
    } else if (input == "goodbye") {
        return "";
    } else {
        return "";
    }
}